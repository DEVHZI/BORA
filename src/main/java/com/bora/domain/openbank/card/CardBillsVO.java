package com.bora.domain.openbank.card;

public class CardBillsVO {
	private String charge_month;
	private String settlement_seq_no;
	private String card_id;
	private String charge_amt;
	private String settlement_day;
	private String settlement_date;
	private String credit_check_type;
	public String getCharge_month() {
		return charge_month;
	}
	public void setCharge_month(String charge_month) {
		this.charge_month = charge_month;
	}
	public String getSettlement_seq_no() {
		return settlement_seq_no;
	}
	public void setSettlement_seq_no(String settlement_seq_no) {
		this.settlement_seq_no = settlement_seq_no;
	}
	public String getCard_id() {
		return card_id;
	}
	public void setCard_id(String card_id) {
		this.card_id = card_id;
	}
	public String getCharge_amt() {
		return charge_amt;
	}
	public void setCharge_amt(String charge_amt) {
		this.charge_amt = charge_amt;
	}
	public String getSettlement_day() {
		return settlement_day;
	}
	public void setSettlement_day(String settlement_day) {
		this.settlement_day = settlement_day;
	}
	public String getSettlement_date() {
		return settlement_date;
	}
	public void setSettlement_date(String settlement_date) {
		this.settlement_date = settlement_date;
	}
	public String getCredit_check_type() {
		return credit_check_type;
	}
	public void setCredit_check_type(String credit_check_type) {
		this.credit_check_type = credit_check_type;
	}
	
	

}
